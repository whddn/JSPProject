package gsp;

public class Gsp {
	private int gspID;
	private String userID;
	private String gspDate;
	private String gspContent;
	private String gspTitle;
	private int gspAvailable;
	
	
	public String getGspTitle() {
		return gspTitle;
	}
	public void setGspTitle(String gspTitle) {
		this.gspTitle = gspTitle;
	}
	public int getGspID() {
		return gspID;
	}
	public void setGspID(int gspID) {
		this.gspID = gspID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getGspDate() {
		return gspDate;
	}
	public void setGspDate(String gspDate) {
		this.gspDate = gspDate;
	}
	public String getGspContent() {
		return gspContent;
	}
	public void setGspContent(String gspContent) {
		this.gspContent = gspContent;
	}
	public int getGspAvailable() {
		return gspAvailable;
	}
	public void setGspAvailable(int gspAvailable) {
		this.gspAvailable = gspAvailable;
	}

	
}
